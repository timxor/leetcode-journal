class Solution:
    def isBoomerang(self, points: List[List[int]]) -> bool:
        ax, ay = points[0]
        bx, by = points[1]
        cx, cy = points[2]
        v1 = bx - ax, by - ay
        v2 = cx - ax, cy - ay
        return v1[1] * v2[0] - v1[0] * v2[1] != 0
