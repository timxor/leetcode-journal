int days[] = {0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};

bool isleap(int Y)
{
    return Y%400 == 0 || Y%4 == 0 && Y%100 != 0;
}

class Solution {
public:
    int numberOfDays(int Y, int M) {
        if (isleap(Y) && M == 2)
            return 29;
        else
            return days[M];
    }
};

