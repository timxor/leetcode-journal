class Solution {
public:
    int sumOfDigits(vector<int>& A) {
        int t=*min_element(A.begin(),A.end());
        int cnt=0;
        while(t>0){
            cnt+=t%10;
            t=t/10;
        }
        
        if(cnt%2==0) return 1;
        else return 0;
    }
};
